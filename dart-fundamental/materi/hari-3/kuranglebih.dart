//Operator Perbandingan, Operator yang membandingkan suatu nilai dengan nilai yang lain.
 //Hasil dari perbandingan ini akan dikembalikan dalam tipe data boolean true atau false

void main() {
var number = 17;
print( number < 20 ); // true
print( number > 17 ); // false
print( number >= 17 ); // true, karena terdapat sama dengan
print( number <= 20 ); // true
}